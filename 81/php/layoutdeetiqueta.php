			<p>
			Na etiqueta, temos a coluna "tipo" aonde identificamos se é texte, imagem e etc..</p>
			
			<p>Temos a coluna "Texte/img", que quando logo colocamos o diretorio e quando texto, podemos escrever mensagens livre.</p>

			<p>Na coluna "COLUNA", quando tiver texto, vamos poder informar se vai pegar informação do banco de dados, informando Referencia, grupo e etc.., se for codigo de barra, vamos pode dizer se o codigo de barra é codigo do produto, codigo ean ou referencia e etc.., se for Tabela de preço o sistema vai habilitar a coluna preço para selecionar qual tabela de preço vai ser.</p>

			<p>Na coluna "Altura e Margens", são para colocar aonde inicia a altura e a margem da informação.</p>

			<p>Na coluna "inicio", é para informar o inicio do caracter, Ex a palavra "referencia" se eu quiser que o sistema incie a palavra referencia da 3º letra, eu coloco na coluna inicio 3, dessa forma o sistema vai iniciar da Terceira letra.</p>

			<p>Na coluna "tamanho", é o tamanho de caracteres que vai ter esse registro, podemos limitar para não passar para a outra coluna.</p>
