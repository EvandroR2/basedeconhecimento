									<h3 style="font-size: 18pt;">Quem deve emitir NFS-e?</h3>

									<p>Antes de falarmos exclusivamente da Nota Fiscal de Serviço Eletrônica, é importante ressaltar que toda empresa que comercializa produtos ou serviços têm a obrigação de emitir nota fiscal. Isso só não acontece quando há isenção por lei, algo que é bem raro. <br/><BR>

									No caso da NFS-e, ela deve ser utilizada quando há alguma prestação de serviço, independentemente do tamanho, regime tributário e tipo do negócio.<br/><BR>

									Sendo assim, empresas do Simples Nacional, Empresa de Pequeno Porte (EPP), Lucro Real, Lucro Presumido, MEI (em alguns casos) e até mesmo pessoa física devem emitir o documento fiscal.</p>

									<h3 style="font-size: 18pt;">Como funciona a emissão de NFS-e?</h3>


									<p>A nota fiscal de serviço (NFS-e) deve ser emitida no site da prefeitura em que o serviço foi prestado. O principal desafio é que os órgãos municipais não possuem um padrão de emissão, podendo exigir certificado digital, login e senha e muito mais.<br/><BR>

									Apesar da maioria das cidades já ter aderido ao modelo eletrônico, ainda não são todas que contam com essa facilidade. Portanto, se for possível, o mais indicado é você procurar um município que trabalhe com a NFS-e.<br/>  <BR>

									Depois disso, é preciso fazer um credenciamento da sua empresa no site da prefeitura, podendo ser MEI, Simples Nacional, Microempresa ou qualquer outro tipo.</p>
