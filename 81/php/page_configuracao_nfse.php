									<h3 style="font-size: 18pt;">O que é Nota Fiscal de Serviço Eletrônica (NFS-e)?</h3>

									<p>A Nota Fiscal de Serviço Eletrônica, como o próprio nome diz, deve ser utilizada quando há prestação de serviço de qualquer natureza, como clínicas médicas, escolas, academias e consultorias em geral.  <br/><BR>

									Sendo assim,  é um documento que formaliza a transação entre comprador e vendedor, seja no ambiente físico ou no digital. A NFS-e ainda pode ser utilizada como recibo e tem validade jurídica e fiscal.<br/><BR>

									Com o surgimento do modelo eletrônico, não é necessário mais gerar a nota em papel. Isso representa uma economia significativa com material físico, gráfica, estoque e espaço para armazenamento.<br/><BR>

									No caso da NFS-e, a emissão é realizada pelo site da prefeitura do município em que a empresa prestadora está sediada.<br/><BR>

									A maioria das cidades brasileiras já aderiram ao documento digital, e outras estão implantando gradativamente.<br/><BR>

									Além da NFS-e, há outros tipos que são bastante utilizados no Brasil, como a Nota Fiscal de Produto Eletrônica (NF-e) e a Nota Fiscal de Consumidor Eletrônica (NFC-e).<br/><BR>

									Basicamente, a primeira é utilizada para a venda de produtos físicos e a segunda para substituir o cupom fiscal no comércio.
