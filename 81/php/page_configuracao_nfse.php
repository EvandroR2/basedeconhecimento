									<p>O Recibo Provisório de Serviços (RPS) é um documento utilizado quando ocorre algum problema para gerar a nota fiscal, como falta de luz ou de internet.</p>

									<p>Ele é muito usado por hotéis, academias, escolas e estacionamentos, já que, geralmente, o cliente não pode esperar pelo documento.</p>

									<p>Dessa forma, o RPS serve como uma alternativa à geração de NFS-e, já que é entregue ao cliente de forma ágil.</p>

									<p>Porém, ele não substitui a Nota Fiscal de Serviço, é apenas uma solução em casos de emergência. Sendo assim, o RPS deve ser convertido em nota fiscal dentro do prazo estabelecido por cada prefeitura.</p>


