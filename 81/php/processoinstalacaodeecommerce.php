		<h1>Processo tecnico para o sistema</h1>
			<h2>Instalação de E-commerce</h2>

			<p>A instalação do E-commerce, é feita remotamente, e sempre com a presença do responsavel que gerencia o sistema PDVNET da sua empresa, é importante deixar o cliente ciente que caso ele não seja o responsavel da plataforma, deve deixar o desenvolvedor da plataforma informado do agendamento feito para a configuração da plataforma, porque deve precisar de alguns dados, não é possivel definir os dados porque são informações que o tecnico vai levantar no momento da configuração.</p>

			<p>O Tecnico vai conectar com o cliente e vai precisar definir informaçoes do sistema para configurar o e-commerce, principais levantamentos do tecnico com o cliente:</p>
	</div>
	<div class="coluna col12">
			<ul class="coluna col10" style="color: #000000; list-style-type: square;">
				<li>Verificar qual vai ser a filial que vai ser faturado a nota fiscal.</li>
				<li>Verificar qual filial ou quais filiais será composto o estoque do ecommerce.</li>
				<li>Verificar se o cliente vai trabalhar com uma tabela de venda diferente para o e-commerce.</li>
				<li>Verificar e explicar sobre os campos que hoje o sistema sobe para a plataforma</li>
				<li>Verificar com o cliente sobre a questão de integrar as transportadoras e se plataforma tem como configurar.</li>
				<li>Verificar com o cliente se ao baixar o pedido da plataforma, o sistema faz a separação automatica (caso tenha mais do que uma filial para compor o estoque explicar a função e passar que não é possivel fazer com mais do que uma compondo o estoque).</li>
			</ul>
	</div>
	<div class="coluna col12">

			<p>Hoje podemos dizer que são os levantamentos que devemos fazer hoje para configurar uma plataforma com informaçoes do sistema, agora devemos ativar o "INTEGRADOR" no suporte.exe e atualizar o sistema, lembre-se que o PDVINTEGRADOR, só é baixado quando o suporte.exe esta com a função ativa no cliente.</p>

			<p>Abrindo o PDVINTEGRADOR depois de atualizar o sistema, lembrando que o PDVINTEGRADOR é um modulo que fica em 2º plano, então deve ir nos icones dentro do canto do relogio e clicar nele para abrir, podemos ver temos a opção de exportar e baixar</p>
			<p>Exportar: serve para voce subir as informaçoes do produto, Referencia, preço, saldo, codigo de produto (SKU).</p>
			<p>Baixar: serve para o PDVINTEGRADOR ir na plataforma e verificar se tem alguma compra no site, se tiver, ele vai baixar como um "PEDIDO DE ENTREGA" e o sistema vai deixar como uma venda e dependendo da parametrização, o pedido pode estar para separar a mercadoria ou ja para emissão da nota fiscal.</p>

			<p>Agora não é a hora de verificar essa questão de exportar, vamos configurar a plataforma, então no integrador, podemos ver a opção de configuração na parte superior, abrindo outra opção, vamos em configurar.<br/>
			vamos ver as plataformas cadastradas, não tendo a nossa, vamos em INCLUIR.</p>

			<p>Aqui vamos colocar os dados que levantamos na primeira parte com o cliente.</p>
			<p>E na sua lateral da esquerda, vamos selecionar a plataforma e ir no botão CONFIGURAR PLATAFORMA.</p>
