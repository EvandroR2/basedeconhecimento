<h2>0 - Introdução</h2>

<p>Para um bom funcionamento do sistema é necessário que sejam seguidos alguns padrões de cadastros, para que as informações recebidas na franqueadora sejam as mais precisas possíveis, nesse manual serão listados os cadastros de responsabilidade do franqueador e franqueado, bem como o procedimento que deve ser adotado para instalação de uma loja. </p>

<h2>1 - Cadastro de responsabilidade do Franqueador:</h2>

<p>Saber qual cadastro é de responsabilidade da franquia ou do franqueador é muito importante, pois nesse caso saberemos se é possível ou não realizar o cadastro no sistema na franquia, serão listados agora todos os cadastros de responsabilidade do franqueador. Por este motivo os dados listados não devem ser cadastrados na franquia pois o mesmo tem o recebimento automático, bastando apenas realizar o recebimento de dados através da ferramenta PDVSYNC.</p>

<p>Vemos no fluxograma acima que tudo o que se refere a produto é de responsabilidade do franqueador, ou seja, cadastro de fornecedores, preços, grades, cores, tributos (alíquota de ICMS, substituição de ICMS, NCM, e CEST), havendo alguma divergência no cadastro do produto o franqueado deve se reportar ao franqueador para realizar o ajuste, por se tratar de um cadastro central, para toda a rede da franquia, nenhuma alteração deve ser realizada na franquia sob pena de ter os dados subscrevidos pelo franqueador, por este motivo não é realizado treinamento de cadastro de produto na franquia, sendo ensinado somente a consulta dos dados da tela.</p><br/>
<p>Observamos ainda que o cadastro de transportadoras, tipos de desconto e as bandeiras de cartões também são cadastradas na franqueadora, havendo necessidade de um novo cadastro de uma transportadora ou uma bandeira de cartão que opera na região do franquiado o mesmo deve solicitar ao franqueador a criação do registro. </p>
