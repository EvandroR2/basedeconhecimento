<h2>4 – Processo de instalação de loja (franquia):</h2>

<p>A instalação de uma loja da franquia é feita através de solicitação junto ao comercial da PDVNet , que após a confirmação dos dados via e-mail,  libera para o setor de implantação realizar contato ocorrendo assim o  agendamento do serviço. Este contato poderá ser feito por telefone ou e-mail, e tem seu agendamento confirmado sempre um dia útil antes de cada data agendada. Algumas importante observações sobre o processo de implantação da loja.</p>

<p> &#10151;	A PDVNet será responsável apenas pela instalação dos sistemas PDVNet. Qualquer outro Software ou Hardware devem ser instalados pelo técnico de informática do Franqueado.</p>
<p> &#10151;	O computador deve estar montado e ligado para que o técnico da PDVNet possa realizar o serviço de instalação do sistema, qualquer tipo de hardware que fará comunicação com o sistema PDVNET deverá estar previamente instalado, por exemplo: IMPRESSORA NÃO FISCAL ou CERTIFICADO DIGITAL. Caso estes não estejam instalados, o técnico da PDVNET, por sua vez, fará a finalização de sua parte de instalação deixando o sistema apto para uso e comunicará ao Franqueado a necessidade de acertar os itens faltantes.</p>
<p> &#10151;	Vale ressaltar, que o Franqueado deve cadastrar os vendedores, usuários e configurar os cartões utilizados na loja. O técnico da PDVNet poderá auxiliar, mas o cadastro é de obrigação do Franqueado, os dados fiscais da loja, serão preenchidos pelo franqueado com o auxílio do técnico da implantação, caso o franqueado não possua os dados poderá acionar a sua contabilidade para pegar o mesmo.</p>
