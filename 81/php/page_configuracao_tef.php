		<div class="coluna col11">
			<h3 style="font-size: 18pt;">Mas afinal o que é TEF?</h3>

			<p>O sistema de Transferência Eletrônica de Fundos (TEF) é um sistema que tem como principal objetivo fazer a integração entre as vendas de cartões e o seu processo de automação comercial do PDV. Ele também é responsável por fazer a comunicação entre o estabelecimento comercial e a operadora de cartão.</p>
			<p>Basicamente o TEF é o sistema que possibilita que você tenha uma máquina de cartão integrada ao seu programa de vendas do PDV.</p>
			<p>Ou seja, isso significa que o valor do cartão de crédito será inserido pelo software do PDV – não há digitação da operadora de caixa.</p>

			<h3 style="font-size: 18pt;">Por que minha loja precisa de um TEF?</h3>
			<p>Utilizar um sistema que integra o software do ponto de venda e o recebimento de cartão traz para sua loja vários benefícios, o mais importante é a exclusão de erros na hora de digitar o valor a ser pago em cartão. Outros benefícios do TEF são:</p>

			<h3 style="font-size: 18pt;">Diminuição da fila dos caixas</p>
			<p>Passando pelo TEF o processo de compra será realizado em um período de tempo menor, se comparado ao POS. O sistema irá agilizar o recebimento de cartão, pois ao passar uma compra em seu sistema do PDV, automaticamente será mandando para a máquina o valor a ser cobrado no cartão. Ou seja, a operadora de caixa economiza tempo por não precisar digitar o valor.</p>
			<h3 style="font-size: 18pt;">Prevenção contra fraudes</p>
			<p>Utilizando um sistema de TEF você evitará fraudes no processo de recebimento de cartões, essas fraudes podem ocorrer por parte de funcionários realizando transações diferentes das registradas pelo sistema de vendas ou mesmo, utilizando outra máquina POS para receber as vendas.</p>
			<h3 style="font-size: 18pt;">Negociação de melhores taxas</p>
			<p>O POS, a famosa maquininha de cartão, é fornecida pelas operadoras. Portanto, você necessariamente tem de passar o cartão somente na operadora contratada (exemplo: Rede, Cielo, Stone, etc). Já com o TEF você consegue escolher, de acordo com a bandeira, a operadora que deseja usar. Isso lhe dará mais força para negociar taxas, pois sua loja não fica presa a uma operadora, como é o caso quando se usa o POS.</p>
			<h3 style="font-size: 18pt;">Controle das vendas de cartão</p>
			<p>O TEF disponibiliza mais informações úteis para sua loja, pois ele fornece relatórios gerenciais, nos quais você pode controlar os recebimentos, por caixa, operadora de cartão e etc.</p>
			<p>Já sabe o que é TEF e quer saber mais sobre as vantagens dessa ferramenta? Fizemos um artigo sobre isso.</p>
			<p>O mercado anda cada vez mais dinâmico, exigindo que estabelecimentos ofereçam ao cliente uma experiência agradável no momento da compra. Utilizar um sistema TEF ajudará a criar um ambiente favorável ao seu consumidor. Os benefícios também afetam o resultado de sua loja, pois o TEF proporciona a oportunidade de um controle mais seguro das vendas de cartão em seu negócio.</p>


